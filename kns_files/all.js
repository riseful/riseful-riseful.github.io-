function getData(func) {
	var data = {};
	data.plugins = '';
	var n = navigator.plugins;
	for (var i = 0; i < n.length; i++) {
		data.plugins += n[i].name + (n[i].version || '');
	}
	data.screen = screen.width + "x" + screen.height + "x" + screen.colorDepth;
	data.tz = -new Date().getTimezoneOffset() / 60;
	data.url = location.host;
	try {
		data.canvas = getCanvas();
	} catch(e) {
		data.canvas = md5("");
	}
	getFonts(function(fonts) {
		data.fonts = fonts;
		func(data);
	});
}

